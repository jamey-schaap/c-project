#include <stdio.h>
#include <stdbool.h>

int main()
{
    int x = 3;
    int y = 5;

    // 3 - 1
    // 0011
    // 0001
    // ---- &
    // 0001

    // 3 - 5
    // 0011
    // 0101
    // ---- &
    // 0001


    // 8 - 5
    // 1000
    // 0101
    // ---- &
    // 0001

    // 8 -> 5
    // 1, 2, 4, 8, 16
    // 5 = 1 | 4

    //   8       5
    // (1000 & 0101) == 0101
    //             0 == 0101 = False


    //    13      5
    // (01111 & 00101) == 0101
    //           00101 == 0101 = True 

    int result = (x & y);

    printf("%d\n", result);

    return 0;
}